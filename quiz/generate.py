def _gen_vocab_group(word_dict, n, fwd_lv, rev_lv):
    from collections import defaultdict
    use = dict(list(word_dict.items())[:n])
    by_meaning = defaultdict(list)
    for w, m in use.items():
        by_meaning[m].append(w)
    words = list(use.keys()); meanings = list(use.values())
    for w, m in use.items():
        add(f"영어 단어 '{w}'의 뜻은?", m, meanings, "LANG", fwd_lv)
        if len(by_meaning[m]) == 1:   # 뜻이 유일할 때만 역방향(정답 중복 방지)
            pool = [x for x in words if use[x] != m]
            add(f"'{m}'을(를) 뜻하는 영어 단어는?", w, pool, "LANG", rev_lv)

