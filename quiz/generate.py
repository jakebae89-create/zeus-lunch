import json, random
random.seed(20260613)

BANK = []
def add(q, correct, distractor_pool, cat, lv):
    """정답 1개 + 풀에서 서로 다른 오답 3개로 4지선다 생성."""
    opts = [d for d in dict.fromkeys(distractor_pool) if d != correct]
    if len(opts) < 3:
        return  # 오답 후보 부족하면 건너뜀
    wrong = random.sample(opts, 3)
    choices = wrong + [correct]
    random.shuffle(choices)
    BANK.append({"q": q, "c": choices, "a": choices.index(correct), "cat": cat, "lv": lv})

def add_fixed(q, choices, a, cat, lv):
    BANK.append({"q": q, "c": list(choices), "a": a, "cat": cat, "lv": lv})

