# (시작월, 시작일) — 통용되는 경계
ZODIAC_START = [(12,22,"염소자리"),(1,20,"물병자리"),(2,19,"물고기자리"),(3,21,"양자리"),
                (4,20,"황소자리"),(5,21,"쌍둥이자리"),(6,22,"게자리"),(7,23,"사자자리"),
                (8,23,"처녀자리"),(9,23,"천칭자리"),(10,23,"전갈자리"),(11,22,"사수자리")]
DAYS_IN_MONTH = [31,28,31,30,31,30,31,31,30,31,30,31]

def sign_of(m, d):
    # 해당 월의 시작 경계와 비교
    boundaries = {sm:(sd,name) for (sm,sd,name) in ZODIAC_START}
    # 이번 달에 시작하는 별자리 경계
    if m in boundaries:
        sd, name = boundaries[m]
        if d >= sd:
            return name
    # 그렇지 않으면 직전 달에 시작한 별자리
    pm = 12 if m == 1 else m - 1
    # pm월에 시작한 별자리 찾기
    for (sm, sd, name) in ZODIAC_START:
        if sm == pm:
            return name
    return None

def is_boundary(m, d):
    for (sm, sd, name) in ZODIAC_START:
        if sm == m and abs(d - sd) <= 1:
            return True
    return False

def gen_zodiac():
    for m in range(1, 13):
        for d in range(1, DAYS_IN_MONTH[m-1] + 1):
            if is_boundary(m, d):       # 경계(±1일) 날짜는 논쟁 여지 → 제외
                continue
            s = sign_of(m, d)
            if s:
                add(f"{m}월 {d}일에 태어난 사람의 별자리는?", s, ZODIAC_SIGNS, "GEN", 2)

